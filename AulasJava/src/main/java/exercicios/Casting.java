/*
    Declare uma variável do tipo double e uma variável do tipo int. Faça o casting da variável
    double para int e imprima o resultado.
 */

package exercicios;

public class Casting {
    public static void main(String[] args) {

        double numero1 = 1.4;
        int numero2 = 10;

        System.out.println("Número declarado: " + numero1 + " Casting double para int: "+ (int)numero1);
    }
}
